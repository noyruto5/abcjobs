<h1>About Sagay City ABC Hall</h1>
<br/>
<p>ABC Hall (PESO OFFICE) is a government office in Sagay City, Negros Occidental which aims to give
an opporturnity and a chance to every Sagaynon to land a job by offering a program called Summer Job
 for students once a year as well as an application to those who wanted to apply locally and abroad.
The office also conducts meeting and session regarding the projects barrowed equipment in each barangay.</p>

<p>This office is responsible for the processing of transaction of Summer Job and Job Hiring.</p>

<p>In the summer job and job hiring for applicants, it requires some requirements to avail the slot.</p>

<p>The ABC Hall (PESO OFFICE) purpose is to help the sagaynon land a job and to help those people to improve the status of life of the different sagaynons.</p>

